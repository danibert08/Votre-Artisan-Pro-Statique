3. Tarifs
3.1 Frais de création
79 € TTC payables à la commande.
3.2 Abonnement mensuel
12,99 € TTC par mois, sans engagement minimum (ou préciser si engagement).
Les tarifs peuvent être modifiés à tout moment.
Toute modification s’applique au renouvellement suivant.
4. Modalités de paiement
Le paiement s’effectue par carte bancaire ou tout autre moyen proposé sur le site.
L’abonnement est prélevé automatiquement chaque mois.
En cas d’échec de paiement :
Suspension du site après 7 jours
Résiliation possible après 30 jours sans régularisation
5. Durée et résiliation
5.1 Durée
L’abonnement est conclu pour une durée indéterminée.
5.2 Résiliation par le client
Le client peut résilier à tout moment depuis son espace client ou par email.
La résiliation prend effet à la fin de la période mensuelle en cours.
Les frais de création (79 €) ne sont pas remboursables.
6. Sous-domaine et nom de domaine
Par défaut, le site est publié sous un sous-domaine appartenant au Prestataire.
En cas de souscription à un nom de domaine personnalisé :
Celui-ci est enregistré au nom du client (si possible)
Le renouvellement est soumis au paiement de l’abonnement
En cas de résiliation :
Le sous-domaine est désactivé
Le nom de domaine peut être transféré au client sous réserve de règlement des frais éventuels
7. Obligations du client
Le client s’engage à :
Fournir des informations exactes
Disposer des droits sur les contenus transmis (textes, images, logos)
Ne pas publier de contenu illégal ou diffamatoire
Le client reste seul responsable du contenu publié.
8. Propriété intellectuelle
Le Prestataire conserve la propriété :
De la structure technique du site
Du design générique
Du code et de la plateforme
Le client conserve la propriété de ses contenus.
En cas de résiliation, le site peut être supprimé.
Aucune restitution du code source n’est garantie.
9. Responsabilité
Le Prestataire s’engage à fournir un service fonctionnel.
Il ne peut être tenu responsable :
D’une interruption temporaire
D’une perte de données imputable au client
D’un manque à gagner
Du référencement naturel
La responsabilité est limitée au montant des sommes versées au cours des 12 derniers mois.
10. Maintenance et disponibilité
Le Prestataire assure :
L’hébergement
Les mises à jour techniques nécessaires
La sécurité raisonnable du site
Des interruptions temporaires peuvent survenir pour maintenance.
11. Données personnelles
Les données collectées sont nécessaires à l’exécution du contrat.
